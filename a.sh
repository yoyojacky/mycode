#!/bin/bash
# i2c nightlight
# 

while true
	do 
		for i in `seq 1 9`
		do 
			for x in a b c d e f 
			do 
				i2cset -y 1 0x15 0x0$i 0x00
			done 
		done 
	#第一排
		i2cset -y 1 0x15 0x01 0x55
